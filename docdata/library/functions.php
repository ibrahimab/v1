/**
 * Function that tries to find the ISO country code from the country name entered by the users when making a booking
 *
 * @param string $clientCountry		The country name provided by the user
 * @param string $websiteCode			The website code
 *
 * @return string | false
 */
function docdata_iso_landcode($clientCountry, $websiteCode = "") {

	global $customCountriesList, $countriesList;

	$iso_landcode = false;

	// For Belgium orders we set default country to BE
	if ($websiteCode == "B") {
		$iso_landcode = "BE";
	} else {
		# Search for the country code in the countries list
		if (!empty($clientCountry)) {
			# Check if the client countries has a special name
			if(isset($customCountriesList) && is_array($customCountriesList)) {
				foreach($customCountriesList as $key => $value) {
					if($clientCountry == $key || strtolower($clientCountry) == strtolower($key)) {
						$clientCountry = $value;
					}
				}
			}
			if (!empty($clientCountry)) {
				# Search in NL country names
				if(isset($countriesList["nl"]) && is_array($countriesList["nl"])) {
					foreach($countriesList["nl"] as $key => $value) {
						if($clientCountry == $value || strtolower($clientCountry) == strtolower($value)) {
							$iso_landcode = $key;
						}
					}
				}
				if($iso_landcode === false) {
					# Search in EN country names
					if(isset($countriesList["en"]) && is_array($countriesList["en"])) {
						foreach($countriesList["en"] as $key => $value) {
							if($clientCountry == $value || strtolower($clientCountry) == strtolower($value)) {
								$iso_landcode = $key;
							}
						}
					}
				}
				if($iso_landcode === false) {
					# Search in DE country names
					if(isset($countriesList["de"]) && is_array($countriesList["de"])) {
						foreach($countriesList["de"] as $key => $value) {
							if($clientCountry == $value || strtolower($clientCountry) == strtolower($value)) {
								$iso_landcode = $key;
							}
						}
					}
				}
			}
		}
	}

	return $iso_landcode;
}
